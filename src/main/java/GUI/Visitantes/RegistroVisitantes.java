    private void BtnGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnGuardarMouseClicked
        int id = Integer.parseInt(TxtID.getText());
        String nombre = TxtNombres.getText();
        String apellidos = TxtApelllidos.getText();
        String Tipdoc = ComboTipoDoc.getSelectedItem().toString();
        String apartamento = TxtApartamento.getText();
        String edad = TxtEdad.getText();
        String vehiculo = ComboTipoVehiculo.getSelectedItem().toString();

        try {
            Connection con = new CConexion().establecerConexion();
            PreparedStatement ps = con.prepareStatement("insert into RegistroVisitantes (idVisi,nombreVisi,apellidosVisi,tipoDocVisi,apartamentoVisi,edadVisi,vehiculo) values (?,?,?,?,?,?,?)");

            ps.setInt(1, id);
            ps.setString(2, nombre);
            ps.setString(3, apellidos);
            ps.setString(4, Tipdoc);
            ps.setString(5, apartamento);
            ps.setString(6, edad);
            ps.setString(7, vehiculo);

            ps.executeUpdate();

            JOptionPane.showMessageDialog(null, "Agregado Correctamente");

            ps.close();
            con.close();
            limpiar();
            conteoCarro();
            conteoMoto();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_BtnGuardarMouseClicked

