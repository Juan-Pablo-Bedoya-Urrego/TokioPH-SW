    private void conteoCarro() {
        try {
            Connection con = new CConexion().establecerConexion();

            PreparedStatement ps = con.prepareStatement("SELECT COUNT(vehiculo) FROM RegistroVisitantes WHERE vehiculo = 'Carro'");

            ResultSet rs = ps.executeQuery(); 

            if (rs.next()) { 
                int numeroRegistros = rs.getInt(1); 
                int resl = contCarros - numeroRegistros; 
                String Mostrar = Integer.toString(resl); 
                TxtNumParqueaderos.setText(Mostrar); 
            }

            rs.close(); 
            ps.close(); 
            con.close(); 
        } catch (Exception e) {
        }
    }
    
    private void conteoMoto(){
         try {
            Connection con = new CConexion().establecerConexion();

            PreparedStatement ps = con.prepareStatement("SELECT COUNT(vehiculo) FROM RegistroVisitantes WHERE vehiculo = 'Moto'");

            ResultSet rs = ps.executeQuery(); 

            if (rs.next()) { 
                int numeroRegistros = rs.getInt(1); 
                int resl = contMotos - numeroRegistros; 
                String Mostrar = Integer.toString(resl); 
                TxtNumParqueaderos1.setText(Mostrar); 
            }

            rs.close(); 
            ps.close(); 
            con.close(); 
        } catch (Exception e) {
        }
    }

